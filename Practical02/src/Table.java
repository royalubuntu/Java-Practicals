
public class Table
{
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=10;i++)
        {
            System.out.println("\nTable of "+i);
            for(j=1;j<=10;j++)
            {
                System.out.println(i + " x "+j+" = "+(i*j));
            }

        }
    }
}
