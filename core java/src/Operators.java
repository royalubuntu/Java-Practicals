public class Operators
{
    public static void main(String args[])
{
    int a = 10;
    int b = 12;
    int c = 12;
    System.out.println(a>b && b==c); // (&) AND Operator if all  Condition are true  then it returns ture & if any 1 codition is false then return False

    System.out.println(a>b || b==c); // (|) OR Operator if any Condition is True then it Returns True .
    System.out.println(c!=b); // Returns Vice-Verse of Given Condition means if Condition is True then return False.




}
}
