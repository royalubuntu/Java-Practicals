public class SpecialCharacters
{
    public static void main(String args[])
    {
        String str = "Myself \'Musaib\' Ismail Tamboli ";
        System.out.println(str);
    }
}
/*
 Escape chr   Result        Description

    \'	        '	        Single quote
    \"      	"	        Double quote
    \\      	\	        Backslash
 */