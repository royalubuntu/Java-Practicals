package Arithmatic;

public class Addition
{
    public static int addNum(int a, int b)
    {
        return (a+b);
    }
    public static int addNum(int a, int b,int c)
    {
        return (a+b+c);
    }
    public static int addNum(int a, int b,int c,int d)
    {
        return (a+b+c+d);
    }
    public static int addNum(int a, int b,int c , int d,int e)
    {
        return (a+b+c+d+e);
    }

}
